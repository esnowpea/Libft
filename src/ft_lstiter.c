#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*tmp;
	t_list	*next;

	if (!lst || !f)
		return ;
	tmp = lst;
	next = tmp->next;
	while (tmp)
	{
		(*f)(tmp);
		tmp = next;
		if (tmp)
			next = tmp->next;
	}
}
