#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t i;
	size_t j;

	i = ft_strlen(dest);
	j = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (src[j] != '\0' && i + j < size - 1)
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	j = ft_strlen(src);
	if (size < i)
		return (j + size);
	else
		return (j + i);
}
